    private void txt_buscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_buscarKeyPressed

        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("APELLIDO");
        modelo.addColumn("CEDULA");
        modelo.addColumn("EDAD");

        tabla_busqueda.setModel(modelo);
        String sql = "SELECT * FROM empleado WHERE Nombre_Emple LIKE '%" + txt_buscar.getText() + "%' "
                + "OR Apellido_Emple LIKE '%" + txt_buscar.getText() + "%'" + "OR Cedula_Emple LIKE '%" + txt_buscar.getText() + "%'";
        String datos[] = new String[5];

        try {

            cc.st = cc.conexion.createStatement();
            cc.rs = cc.st.executeQuery(sql);

            while (cc.rs.next()) {
                datos[0] = cc.rs.getString(1);
                datos[1] = cc.rs.getString(2);
                datos[2] = cc.rs.getString(3);
                datos[3] = cc.rs.getString(4);
                datos[4] = cc.rs.getString(9);

                modelo.addRow(datos);

            }
            tabla_busqueda.setModel(modelo);
        } catch (Exception e) {
            System.out.println("ERROR AL BUSCAR LOS DATOS : " + e.getMessage());
            Logger.getLogger(VentanaInformes.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_txt_buscarKeyPressed

