    private void mostrartable() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("APELLIDO");
        modelo.addColumn("CEDULA");
        modelo.addColumn("EDAD");

        grilla_funcionario.setModel(modelo);
        String sql = "SELECT * FROM empleado";
        String datos[] = new String[5];

        try {

            rs = cc.listar_datos(sql);

            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(9);

                modelo.addRow(datos);

            }
            grilla_funcionario.setModel(modelo);
        } catch (Exception e) {
            Logger.getLogger(Agregar_Usuario.class.getName()).log(Level.SEVERE, null, e);
        }
    }

    public void Actualizardatos() {
        try {
            String SQL = "update empleado set  Nombre_Emple=?,Apellido_Emple=?,Cedula_Emple=?,Emple_Edad=? where idEmpleado=? ";

            int filaSeleccionada = grilla_funcionario.getSelectedRow();
            String dao = (String) grilla_funcionario.getValueAt(filaSeleccionada, 0);

            java.sql.PreparedStatement pst = cn.prepareStatement(SQL);

            pst.setString(1, txtname.getText());
            pst.setString(2, txtape.getText());
            pst.setDouble(3, Double.parseDouble(txtced.getText()));
            pst.setDouble(4, Double.parseDouble(txtedad.getText()));

            pst.setString(5, dao);
            pst.execute();

            JOptionPane.showMessageDialog(null, "Registro Actualizado con Exito");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al Actualizar el Registro" + e.getMessage());
        }
    }

