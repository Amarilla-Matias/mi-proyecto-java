    private void mostrartabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");

        grilla_empleado.setModel(modelo);
        String sql = "SELECT * FROM `empleado`";
        String datos[] = new String[3];

        try {

            con.st = con.conexion.createStatement();
            con.rs = con.st.executeQuery(sql);

            while (con.rs.next()) {
                datos[0] = con.rs.getString(1);
                datos[1] = con.rs.getString(2);
                datos[2] = con.rs.getString(3);

                modelo.addRow(datos);

            }
            grilla_empleado.setModel(modelo);
        } catch (Exception e) {
            Logger.getLogger(Buscar_Funcionario.class.getName()).log(Level.SEVERE, null, e);
        }

    }
