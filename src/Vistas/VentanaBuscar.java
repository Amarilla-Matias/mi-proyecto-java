    private void txtbuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbuscarKeyPressed
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("PATENTE");
        modelo.addColumn("HORA DE OCUPACION");
        modelo.addColumn("TIPO DE AUTO");
        modelo.addColumn("PISO");

        tablaBuscar.setModel(modelo);
        String sql = "SELECT * FROM cocheras WHERE patente LIKE '%" + txtbuscar.getText() + "%' "+ "OR piso LIKE '%" + txtbuscar.getText() + "%'" + "OR patente LIKE '%" + txtbuscar.getText() + "%'";
        String datos[] = new String[4];

        try {

            bdm.st = bdm.conexion.createStatement();
            bdm.rs = bdm.st.executeQuery(sql);

            while (bdm.rs.next()) {
                datos[0] = bdm.rs.getString(2);
                datos[1] = bdm.rs.getString(3);
                datos[2] = bdm.rs.getString(4);
                datos[3] = bdm.rs.getString(5);

                modelo.addRow(datos);

            }
            tablaBuscar.setModel(modelo);
        } catch (Exception e) {
            System.out.println("ERROR AL BUSCAR LOS DATOS : " + e.getMessage());
            Logger.getLogger(VentanaInformes.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_txtbuscarKeyPressed


