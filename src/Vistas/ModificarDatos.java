    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        int eliminar;
        eliminar = grilla_cochera.getSelectedRow();
        if (eliminar == -1) {
            JOptionPane.showMessageDialog(null, "No se ha seleccionado ninguna fila");
        } else {
            int valor = JOptionPane.showConfirmDialog(null, "Seguro desea eliminar?", "Advertencia", JOptionPane.YES_NO_OPTION);
            if (valor == JOptionPane.YES_OPTION) {
                int fila = grilla_cochera.getSelectedRow();
                String cod = "";
                cod = grilla_cochera.getValueAt(fila, 0).toString();
                try {
                    java.sql.PreparedStatement pst = cn.prepareStatement("DELETE FROM cocheras WHERE idcocheras ='" + cod + "'");
                    int n = pst.executeUpdate();
                    if (n > 0) {
                        JOptionPane.showMessageDialog(null, "Registro de Vehiculo Borrado");
                        grilla_cochera.setModel(new DefaultTableModel());
                        limpiar();
                        mostrartabla();
                    } else {
                        JOptionPane.showMessageDialog(null, "El Registro  no se borro");
                    }
                } catch (Exception e) {
                }
            }
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

