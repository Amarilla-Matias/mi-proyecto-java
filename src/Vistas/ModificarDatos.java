    private void mostrartabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("PATENTE");
        modelo.addColumn("PISO");
        modelo.addColumn("TIPO DE AUTO");

        grilla_cochera.setModel(modelo);

        String sql = "SELECT * FROM cocheras";
        String datos[] = new String[4];

        try {

            rs = cc.listar_datos(sql);

            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(5);
                datos[3] = rs.getString(4);

                modelo.addRow(datos);

            }
            grilla_cochera.setModel(modelo);
        } catch (Exception e) {
            Logger.getLogger(ModificarDatos.class
                    .getName()).log(Level.SEVERE, null, e);
        }
    }

    public void Actualizar() {
        try {
            String SQL = "update cocheras set  patente=?,tipo_auto=?,piso=? where idcocheras=? ";

            int filaSeleccionada = grilla_cochera.getSelectedRow();
            String dao = (String) grilla_cochera.getValueAt(filaSeleccionada, 0);

            java.sql.PreparedStatement pst = cn.prepareStatement(SQL);

            pst.setString(1, txtpatente.getText());
            pst.setString(2, txttipo.getText());
            pst.setString(3, txtpiso.getText());

            pst.setString(4, dao);
            pst.execute();

            JOptionPane.showMessageDialog(null, "Registro Actualizado con Exito");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al Actualizar el Registro" + e.getMessage());
        }
    }

    void limpiar() {

        txtpatente.setText("");
        txtpiso.setText("");
        txttipo.setText("");

    }
